package model;

public class FixedCost {

	private float floatingParameterValue;

	public FixedCost(float floatingParameterValue) {
		super();
		this.floatingParameterValue = floatingParameterValue;
	}

	public float getFloatingParameterValue() {
		return floatingParameterValue;
	}

	public void setFloatingParameterValue(float floatingParameterValue) {
		this.floatingParameterValue = floatingParameterValue;
	}
}
